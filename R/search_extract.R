   else if (!missing(coll))
      stri_extract_first_coll(str, coll, ...)
   else if (!missing(charclass))
      stri_extract_first_charclass(str, charclass, ...)
   else
      stop("you have to specify either `regex`, `coll`, or `charclass`")
}


